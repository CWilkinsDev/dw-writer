                <p>
                    For Alfred, the past is a good place. His family is a happy and loving one. His mother is kind, his
                    father is hard-working and respected in the village. Somerset's summer skies are always blue.
                    Somerset's winter landscape glitters white with frost. The Mendip Hills are endlessly beautiful and
                    the tumbling streams are crystal clear. His pals at school are boisterous and fun-loving, like small
                    boys should be. When he goes to work at the big house, the playful maids treat him like their
                    favoured little brother and the butler turns out to have a Jeeves-esque range of endearing hidden
                    talents. When his G.W.R. apprenticeship calls for him to move away from home, his landlady is
                    motherly, the station at which he works buzzes with cheerful rural activity and his station master
                    proves to be a wise and benevolent man.
                </p>
                <p>
                    <i>Schoolboy, Servant, G.W.R. Apprentice</i> takes a gentle stroll through Alfred's life, from the
                    day he
                    started school in 1879, to the day in 1892 when he became, officially and permanently, in his own
                    words, "a uniformed servant of the mighty G.W.R.". Alfred's prose style is simple - almost
                    conversational - but always expressive and engaging. He describes village life vividly and the
                    Mendip landscape with great affection. His memories are full of human interest and of course, well
                    over century on, the historical detail is fascinating.
                </p>
