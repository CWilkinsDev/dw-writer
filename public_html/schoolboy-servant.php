                <p><b>
                        The memoirs of Alfred Plumley, 1810 - 1892
                    </b></p>
                <p>
                    On a cold, thoroughly miserable afternoon in January 2014, in scarf and woolly hat, my warm coat
                    zipped up, I was rooting around in the lots for sale at an auction saleroom on the Dorset coast. The
                    wind howling off the sea rattled the windows. It felt colder indoors than outdoors.
                </p>
                <p>
                    After about half an hour, I was beginning to wish I'd not bothered coming, Then I picked up a
                    creased cardboard folder, clearly some decades old. It contained maybe a hundred typewritten sheets
                    held
                    together in small batches by rusted staples. The paper was tatty and discoloured. The wonky lines
                    suggested that the typewriter's best days were already behind it when the typescript was made. As my
                    cold-numbed fingers turned the pages, the document was revealed to be a memoir of some kind, written
                    by an elderly gent recalling his rural childhood and adolescence. At the sale the following day, a
                    single bid of £20.00 was enough. I was the only buyer interested.
                </p>
                <p>
                    That evening I sat down to read the memoir. The bare bones of it are easily summarised. It was
                    written in the first person - but anonymously. It described the life of a boy growing up in a
                    village in the Mendip Hills in Somerset in the last quarter of the 19th century. It began with the
                    writer's first day of school at the age of five. Just after his twelfth birthday, his schooldays
                    came to end and he went immediately into service as a page-boy at the " big house" nearby. At
                    sixteen, with the help of the kindly lady of the house who wanted to encourage him into a trade, he
                    took
                    up a station staff apprenticeship with the Great Western Railway (G.W.R.). He was placed at a tiny
                    village station, just inland from the Somerset coast. When the writer turned eighteen in 1892, his
                    job
                    with the G.W.R. was confirmed as permanent - and at that point the story ended. The memoirist
                    assured
                    the reader though, that he went on to become a successful railwayman. His entire working life was to
                    be
                    spent with the G.W.R.. </p>
                <p>
                    The typescript itself was undated but piecing together the clues, it was possible to work out that
                    it had been written between 1954 and 1956, when the author was in his early eighties. But who was
                    he? He named himself just twice in the 40,000 words of text, on both occasions reporting other
                    people speaking to him. The first time he referred to himself only by the forename, "Alfred". The
                    second time - in reference to his surname - only by the initial "P". The places where the he lived,
                    went to school and worked were all given fictitious names - a little deceit that dawned on me only
                    after I'd wasted half an afternoon poring over maps of Somerset. Worse still, because the place
                    names were invented, there was of course, also the possibility that that "Alfred", and/or that
                    surname initial "P", were also fabrications. It could easily have been that the memoirist would
                    prove impossible ever to identify. If that had been the case, of course, then I could never have
                    written my book. On page 84 of the typescript however, there was a crucial five digit number. This
                    number - 15718 - proved to be the secret code that would unlock the mystery. It was the number that
                    appeared on the writer's wage slip every week for forty-five years; the number that just possibly
                    might give him a name. It was his G.W.R. staff number.
                </p>
