                <p>
                    The staff records of the G.W.R going back to the middle of the nineteenth century are stored at the
                    National Archives in Kew. The records were not written down sequentially by staff number, so the
                    only way to find our man was to look up every individual "Alfred" who started work with the G.W.R.
                    in the later 1880s or the early 1890s. After I'd ploughed through the first fifty or sixty ghosts of
                    Victorian engine drivers and stokers, signalmen, porters, booking clerks, fitters and coachbuilders
                    named Alfred, the number in the right hand column of the staff records suddenly matched the number
                    in my notebook. There, blinking in the sunlight, after more than a century shut flat in the G.W.R.
                    staff ledger, was the author of the memoir, Alfred James Plumley himself.
                </p>
                <p>
                    A quick reference to the 1881 census records revealed the very same Alfred James Plumley, then a
                    "scholar" aged six, the son of a coachman, living in Lower Langford, a village in the Mendip region
                    of Somerset. It was amazing just how much detail flowed from this first discovery. Alfred's
                    manuscript did not give any real names, even to members of his own family - but public records of
                    the period allowed me to identify them. Likewise I was able to give names to some of the village
                    characters whom Alfred mentions. I was even able to identify the two grand houses at which Alfred
                    worked as a servant by cross-referencing Alfred's clear written descriptions with old photographs of
                    the local area. Gradually, an entire cast of real-life characters emerged to populate Alfred's
                    story.
                </p>
