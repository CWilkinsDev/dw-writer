                <p>
                    I have all sorts of other interests - but the most important in this context is that I have for many
                    years collected old diaries, manuscripts, letters, photo albums and other socio-historical ephemera.
                    I buy them wherever I find them; in auctions, in bookshops, at car boot sales. I always planned that
                    in my retirement I would bring some of the personal stories I had discovered into the public domain.
                    My research skills and experience of writing campaign materials and policy documents in my
                    pre-retirement life proved invaluable in organising for publication the two books I have created so
                    far.
                </p>
