                <p>
                    Welcome. I'm David Wilkins and since I retired from work at the age of sixty in 2015, I've written -
                    or rather, I've partly written and partly edited - a couple of books from original manuscripts by
                    other people. The first book was quite a small project, a personal challenge, to see if I could do
                    it. It was moderately successful but largely only of regional interest in the West Country. The
                    second - <i>The Longest Walk Home</i> - has sold very well in its market and received lots of
                    positive
                    reviews. It is also the book that is my motivation for putting this website together. I'm a private
                    person. I have no social media accounts. I would truly rather not write about myself online. But
                    I've been encouraged to make this website by people who know more about these things than I do. If
                    you want to write books, you need to 'put yourself out there', is the advice. I can see that this
                    makes sense whether I like it or not. So here I am.
                </p>
                <p>
                    It's a strange experience to publish a couple of books when you are in your sixties (as I was for
                    the first one) or your seventies, as I was for the second. In the latter case, it's also been really
                    quite disorientating, mainly because the book has done very well, despite its rather niche market
                    crossover of military history / human interest. Creating a successful book has been like taking the
                    wrong turn on a familiar footpath and finding oneself in an enchanted forest - if that's not a too
                    fanciful a way of describing the world of publishing. Suddenly there are lots of new people and
                    processes in your life. There are editors and proof-readers; marketing teams and PR people and legal
                    advisors. There are serious contracts to sign. There are audition tapes to listen to so you can
                    choose the right voice for audio book. If there's some little detail you aren't happy with in the
                    cover design, the illustrator will probably change it for you. And there are bookshops in every city
                    that have your book on the shelves. You can go into any of these bookshops anytime you like, gaze
                    fondly at your own book and feel pleased with yourself. No-one can stop you. In fact, if you
                    overcome your natural reticence about putting yourself forward, you can even take your book to the
                    counter, introduce yourself and offer to sign all the copies the shop has in stock. And if you do
                    that, the sales assistant will, without fail, be delighted because signed books sell better.
                    Surprisingly also, in my experience, the sales assistant will never ask you to prove you are who you
                    say you are; they will just take your word for it. It's all been a strange experience but it's been
                    an adventure and it's been fun. I'm very grateful to have had it in my life.
                </p>
                <p>
                    I love books. I learned to read when I was at infants' school in the early 1960s and I've not
                    stopped reading since. A rough calculation suggests I've managed comfortably over 1500 books so far
                    in my life. But until I had a crack at it myself I'd never really thought about how difficult it is
                    to make a book actually happen. In the first place, you have to research, write and edit
                    the thing. In the case of <i>The Longest Walk Home</i>, this took hundreds of hours and for me, was
                    every
                    bit as slow and tortuous a process as those celebrated authors always say it is. Next, you have to
                    find a publisher. That was certainly the most difficult bit for me, as it probably is, I think, for
                    most aspiring writers. Ten publishers either turned me down flat or didn't reply. For a short while
                    I even considered giving up on the whole idea - but instead, on the advice of an old friend, I
                    decided on an alternative approach that put me more in control of events. It's a long story - but
                    that decision paid off. It resulted in a publisher - Quercus - finding me. I'll be forever grateful
                    both to them and to my friend Jim, who gave me the intitial advice and made a massive contribution
                    to the process of getting the book published. It was more than six years between my starting work on
                    <i>The Longest Walk Home</i> at my kitchen table to the joyous little miracle of seeing it on
                    bookshop
                    shelves. You can find the full story of the book's long road to publication on the <a
